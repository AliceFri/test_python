from typing import List


class Solution:
    def countPartitions(self, nums: List[int], k: int) -> int:
        # 找出 大于k的数, 和 小于k的数
        _max, _min = [i for i in nums if i >= k], [i for i in nums if i < k]

        ans = 0
        mod = 10**9 + 7
        # 两边都有至少一个_max 里的值
        if len(_max) >= 2:
            ans = (ans + (2 ** len(_max) - 2) * (2 ** len(_min))) % mod

        dp = [0] * (sum(_min) + 10)
        dp[0] = 1
        for b in _min:
            for a in range(len(dp) - 1, -1, -1):
                if a >= b:
                    dp[a] += dp[a - b]

        # _max里的值都在一边
        if len(_max) >= 1:
            # 找出 _min中和 大于k的次数
            a1 = 0
            for t in range(k, len(dp)):
                a1 += dp[t]
            ans = (ans + a1 * 2) % mod

        # 没有_max里的值
        if len(_max) == 0 and sum(_min) >= 2 * k:
            a1 = 0
            for t in range(k, sum(_min) - k + 1):
                a1 += dp[t]
            ans = (ans + a1) % mod

        return ans


